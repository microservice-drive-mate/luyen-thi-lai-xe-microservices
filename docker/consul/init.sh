echo "[Consul] Waiting for Consul to be ready..."

while [ $RETRY_COUNT -lt $MAX_RETRIES ]; do
  if curl -s "$CONSUL_URL/v1/status/leader" > /dev/null 2>&1; then
    echo "[Consul] Consul is ready"
    break
  fi

  RETRY_COUNT=$((RETRY_COUNT + 1))
  echo "[Consul] Waiting... ($RETRY_COUNT/$MAX_RETRIES)"
  sleep 1
done

if [ $RETRY_COUNT -eq $MAX_RETRIES ]; then
  echo "[Consul] Consul failed to become ready"
  exit 1
fi

set_kv() {
  key="$1"
  value="$2"
  display_value="$value"
  case "$key" in
    *password*|*Password*|*secret*|*Secret*|*accountKey*|*AccountKey*)
      display_value="***"
      ;;
  esac
  echo "[Consul] Setting: $key = $display_value"
  # Store as JSON-encoded string so Consul UI JSON mode works correctly
  json_value=$(printf '"%s"' "$(printf '%s' "$value" | sed 's/\\/\\\\/g; s/"/\\"/g')")
  curl -s -X PUT -d "$json_value" "$CONSUL_URL/v1/kv/$key" > /dev/null
}

delete_kv() {
  key="$1"
  echo "[Consul] Deleting optional key: $key"
  curl -s -X DELETE "$CONSUL_URL/v1/kv/$key" > /dev/null
}

seed_media_storage() {
  env_name="$1"
