    delete_kv "config/$env_name/media-service/storage.accountName"
    delete_kv "config/$env_name/media-service/storage.accountKey"
    delete_kv "config/$env_name/media-service/storage.containerName"
    delete_kv "config/$env_name/media-service/storage.presignedUrlExpiry"
